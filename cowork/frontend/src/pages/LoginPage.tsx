import { useState } from 'react'
import { Link, useNavigate } from 'react-router-dom'
import { useAuth, useOAuth } from '../hooks'
import { cn } from '../lib/utils'

export function LoginPage() {
  const navigate = useNavigate()
  const [email, setEmail] = useState('')
  const [password, setPassword] = useState('')
  const { login, isLoading, error, clearError } = useAuth()
  const { startOAuthLogin, isLoading: oauthLoading, error: oauthError, clearError: clearOAuthError } = useOAuth()

  const handleSubmit = async (e: React.FormEvent) => {
    e.preventDefault()
    const success = await login(email, password)
    if (success) {
      navigate('/')
    }
  }

  const handleOAuthLogin = (provider: 'google' | 'github') => {
    clearError()
    clearOAuthError()
    startOAuthLogin(provider)
  }

  const displayError = error || oauthError
  const isAnyLoading = isLoading || oauthLoading

  return (
    <div className="min-h-screen bg-background flex items-center justify-center p-6">
      <div className="w-full max-w-sm">
        {/* Header */}
        <div className="text-center mb-8">
          <h1 className="text-2xl font-medium text-foreground mb-2">Welcome back</h1>
          <p className="text-muted-foreground text-[14px]">Sign in to continue to Cowork</p>
        </div>

        {/* OAuth Buttons */}
        <div className="space-y-3 mb-6">
          <button
            type="button"
            onClick={() => handleOAuthLogin('google')}
            disabled={isAnyLoading}
            className={cn(
              'w-full py-3 px-4 rounded-xl flex items-center justify-center gap-3',
              'bg-secondary border border-border',
              'text-foreground text-[14px] font-medium',
              'hover:bg-accent hover:border-foreground/30 transition-colors',
              'disabled:opacity-50 disabled:cursor-not-allowed'
            )}
          >
            <GoogleIcon />
            Continue with Google
          </button>

          <button
            type="button"
            onClick={() => handleOAuthLogin('github')}
            disabled={isAnyLoading}
            className={cn(
              'w-full py-3 px-4 rounded-xl flex items-center justify-center gap-3',
              'bg-secondary border border-border',
              'text-foreground text-[14px] font-medium',
              'hover:bg-accent hover:border-foreground/30 transition-colors',
              'disabled:opacity-50 disabled:cursor-not-allowed'
            )}
          >
            <GithubIcon />
            Continue with GitHub
          </button>
        </div>

        {/* Divider */}
        <div className="relative mb-6">
          <div className="absolute inset-0 flex items-center">
            <div className="w-full border-t border-border" />
          </div>
          <div className="relative flex justify-center text-[13px]">
            <span className="px-3 bg-background text-muted-foreground">or continue with email</span>
          </div>
        </div>

        {/* Form */}
        <form onSubmit={handleSubmit} className="space-y-4">
          {displayError && (
            <div className="p-3 rounded-lg bg-red-500/10 border border-red-500/20 text-red-400 text-[13px]">
              {displayError}
            </div>
          )}

          <div>
            <label htmlFor="login-email" className="block text-[13px] text-muted-foreground mb-2">Email</label>
            <input
              id="login-email"
              type="email"
              value={email}
              onChange={(e) => {
                setEmail(e.target.value)
                clearError()
                clearOAuthError()
              }}
              placeholder="you@example.com…"
              autoComplete="email"
              required
              className={cn(
                'w-full px-4 py-3 rounded-xl',
                'bg-secondary border border-border',
                'text-foreground text-[14px] placeholder:text-muted-foreground',
                'focus:outline-none focus:border-burnt/50',
                'transition-colors'
              )}
            />
          </div>

          <div>
            <label htmlFor="login-password" className="block text-[13px] text-muted-foreground mb-2">Password</label>
            <input
              id="login-password"
              type="password"
              value={password}
              onChange={(e) => {
                setPassword(e.target.value)
                clearError()
                clearOAuthError()
              }}
              placeholder="••••••••"
              autoComplete="current-password"
              required
              className={cn(
                'w-full px-4 py-3 rounded-xl',
                'bg-secondary border border-border',
                'text-foreground text-[14px] placeholder:text-muted-foreground',
                'focus:outline-none focus:border-burnt/50',
                'transition-colors'
              )}
            />
          </div>

          <button
            type="submit"
            disabled={isAnyLoading}
            className={cn(
              'w-full py-3 rounded-xl',
              'bg-burnt text-white font-medium text-[14px]',
              'hover:bg-burnt/90 transition-colors',
              'disabled:opacity-50 disabled:cursor-not-allowed'
            )}
          >
            {isLoading ? 'Signing in...' : 'Sign in'}
          </button>
        </form>

        {/* Footer */}
        <p className="text-center mt-6 text-[13px] text-muted-foreground">
          Don't have an account?{' '}
          <Link
            to="/register"
            className="text-foreground hover:text-burnt transition-colors"
          >
            Sign up
          </Link>
        </p>
      </div>
    </div>
  )
}

